        energies_sw[j] = energies[j]*sw;

        for (size_t i = 0; i < 31; i++) {
            gxs[i] *= sw;
        }

        vs[0].grads(gxs[0], coords_A_1_a_g, coords_A_1_b_g, coords_A_1_a, coords_A_1_b);
        vs[1].grads(gxs[1], coords_A_1_a_g, coords_B_1_a_g, coords_A_1_a, coords_B_1_a);
